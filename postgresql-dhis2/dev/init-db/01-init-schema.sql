-- Create function to update updated_at timestamp
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ language 'plpgsql';

-- Create trigger to automatically update updated_at
CREATE TRIGGER update_lims_test_results_updated_at BEFORE UPDATE
    ON lims_test_results FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

-- Function to calculate period from test_date (YYYYMM format)
CREATE OR REPLACE FUNCTION calculate_period(test_date DATE)
RETURNS VARCHAR(50) AS $$
BEGIN
    RETURN TO_CHAR(test_date, 'YYYYMM');
END;
$$ LANGUAGE plpgsql;

-- Update period for existing records (if any)
UPDATE lims_test_results SET period = calculate_period(test_date) WHERE period IS NULL;
