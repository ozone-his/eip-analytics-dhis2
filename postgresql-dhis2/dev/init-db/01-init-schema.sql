-- Initialize LIM (Laboratory Information Management) database schema
-- This script creates the necessary tables for PCR test data from LIM systems

-- LIM Test Results Table
-- This table stores raw PCR test data from LIM systems
CREATE TABLE IF NOT EXISTS lims_test_results (
    id SERIAL PRIMARY KEY,
    test_date DATE NOT NULL,
    facility_id VARCHAR(255) NOT NULL,
    facility_name VARCHAR(255),
    test_type VARCHAR(100) NOT NULL DEFAULT 'PCR',
    patient_age INTEGER,
    patient_gender VARCHAR(10),
    test_result VARCHAR(50) NOT NULL, -- 'positive', 'negative', 'indeterminate'
    test_count INTEGER DEFAULT 1,
    period VARCHAR(50), -- Calculated period (e.g., '202401')
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    synced_at TIMESTAMP NULL
);

